        System.out.println("In parallel, ThirstyCalc drives sustainability forward. New terminals");
        System.out.println("automatically recognize whether a user is using their own reusable cup. The");
        System.out.println("system rewards this with small discounts or bonus points. HydraMind analyzes");
        System.out.println("consumption data not just for billing but also for optimizing order logistics.");
        System.out.println("Based on precise forecasts, deliveries are bundled, routes are optimized, and");
        System.out.println("unnecessary trips are avoided. The system can even calculate the CO2 footprint");
        System.out.println("of individual beverage consumption upon request and provide tips for more");
