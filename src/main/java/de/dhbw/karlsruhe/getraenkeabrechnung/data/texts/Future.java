        System.out.println("procedures. Augmented Reality (AR) makes its entry: Users with AR glasses or");
        System.out.println("contact lenses see virtual menus floating above the beverage refrigerators, can");
        System.out.println("retrieve nutritional information with a gaze gesture, or see personalized offers");
        System.out.println("directly in their field of view.");
        System.out.println("");
        System.out.println("Billing becomes more flexible. In addition to the classic pay-per-drink,");
        System.out.println("subscription models (\"Hydration Flatrate\"), credit accounts, and the option to");
